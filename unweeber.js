const human = require('normal-human.js');
const weeb = require('weird-creature.js');
const anime = require('JapanesebombforUS.js');
const dogecoin = require('MuchMoney.js');

if (weeb.saysanything) {
  human.deaf()
} 

if (human.eyes.catch(Anime)) {
  human.brain.InitiateShutDown()
}

if (human.ears.catch(Anime)) {
  human.ears.InitiateDeaf()
}

if (weeb.transformsInto(human)) {
  human.reward(dogecoin)
}
